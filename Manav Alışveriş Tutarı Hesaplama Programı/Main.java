import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        double armut, elma, kivi, muz, domates;
        double tutar;

        Scanner input = new Scanner(System.in);

        System.out.print("Armut Kaç Kg : ");
        armut = input.nextDouble();
        System.out.print("Elma Kaç Kg : ");
        elma = input.nextDouble();
        System.out.print("Kivi Kaç Kg : ");
        kivi = input.nextDouble();
        System.out.print("Muz Kaç Kg : ");
        muz = input.nextDouble();
        System.out.print("Domates Kaç Kg : ");
        domates = input.nextDouble();

        tutar = armut*2.14 + elma*3.67 + kivi*5 + muz*0.95 + domates*1.11;

        System.out.print("Toplam tutar : "+tutar+" TL");
    }
}
