import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        //if else kullanmadan karşılaştırma yaptık
        //degiskenleri tanımladık

        int mat,turkce,fizik,kimya,biyoloji,muzik,tarih;
        double ort ,toplam;

        //scanner sınıfı olusturduk
        Scanner input = new Scanner(System.in);

        //notlarımızı girdik
        System.out.print("Matematik Notunuz : ");
        mat = input.nextInt();

        System.out.print("Türkçe Notunuz : ");
        turkce = input.nextInt();

        System.out.print("Fizik Notunuz : ");
        fizik = input.nextInt();

        System.out.print("Kimya Notunuz : ");
        kimya = input.nextInt();

        System.out.print("Biyoloji Notunuz : ");
        biyoloji = input.nextInt();

        System.out.print("Müzik Notunuz : ");
        muzik = input.nextInt();

        System.out.print("Tarih Notunuz : ");
        tarih = input.nextInt();

        //ortalamayı hesapladık
        toplam = (mat+turkce+fizik+kimya+biyoloji+muzik+tarih);
        ort = toplam/7.0;

        System.out.print("Ortalamanız : " +ort);
        System.out.println(ort > 60 ? " Sınıfı Geçtiniz." : "Sınıfta Kaldınız.");

    }
}
